import json

def pool_mode(label, icon, pairs):
    answers = []
    for _, a in pairs:
        if a not in answers:
            answers.append(a)
    return {
        "label": label, "icon": icon, "description": "",
        "questions": [{"jp": jp, "answer": a, "pool": answers} for jp, a in pairs],
    }

def choices_mode(label, icon, trips):
    return {
        "label": label, "icon": icon, "description": "",
        "questions": [{"jp": jp, "answer": a, "choices": ch} for jp, a, ch in trips],
    }

